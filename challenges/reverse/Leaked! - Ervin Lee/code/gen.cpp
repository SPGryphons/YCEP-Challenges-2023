#include <iostream>
#include <string>
#include <fstream>

using namespace std;

int main() {
    // flag: YCEP2023{4223m8ly_c0D3_15_pr377y_C4NC3r_N9l} split into hex
    char flag[] = {0x59, 0x43, 0x45, 0x50, 0x32, 0x30, 0x32, 0x33, 0x7b, 0x34, 0x32, 0x32, 0x33, 0x6d, 0x38, 0x6c, 0x79, 0x5f, 0x63, 0x30, 0x44, 0x33, 0x5f, 0x31, 0x35, 0x5f, 0x70, 0x72, 0x33, 0x37, 0x37, 0x79, 0x5f, 0x43, 0x34, 0x4e, 0x43, 0x33, 0x72, 0x5f, 0x4e, 0x39, 0x6c, 0x7d};

    cout << flag << endl;

    // array of number to caesar shift each character in the flag array. array is 44 elements long. 
    int shifts[] = {0x1f, 0x2a, 0x3d, 0x4c, 0x5e, 0x6b, 0x7f, 0x8a, 0x9d, 0xa8, 0xb1, 0xc2, 0xd5, 0xe0, 0xf3, 0x0c, 0x1f, 0x2a, 0x3d, 0x4c, 0x5e, 0x6b, 0x7f, 0x8a, 0x9d, 0xa8, 0xb1, 0xc2, 0xd5, 0xe0, 0xf3, 0x0c, 0x1f, 0x2a, 0x3d, 0x4c, 0x5e, 0x6b, 0x7f, 0x8a, 0x9d, 0xa8, 0xb1, 0xc2};

    // caesar shift each character in the flag array
    for (int i = 0; i < sizeof(shifts); i++) {
        flag[i] += shifts[i];
    }


    // print flag
    cout << flag << endl;

    // write encrypted flag to file scraps.txt
    ofstream file("scraps.bin");
    if (file.is_open()) {
        file << flag;
        file.close();
    }
    return 0;
}